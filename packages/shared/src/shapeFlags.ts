export const enum ShapeFlags {
  ELEMENT = 1,
  FUNCTIONAL_COMPONENT = 1 << 1, // 00000001 ==> 00000010 // 2 1*2^1
  STATEFUL_COMPONENT = 1 << 2, // 00000001 ==> 00000100 // 4 1*2^2
  TEXT_CHILDREN = 1 << 3, // 文本 00000001 ==> 00001000 // 8 1*2^3
  ARRAY_CHILDREN = 1 << 4, // 数组 00000001 ==> 00010000 // 16 1*2^4
  SLOTS_CHILDREN = 1 << 5, // 00100000
  TELEPORT = 1 << 6, // 01000000
  SUSPENSE = 1 << 7, // // 10000000
  COMPONENT_SHOULD_KEEP_ALIVE = 1 << 8, // 100000000
  COMPONENT_KEPT_ALIVE = 1 << 9, // 1000000000
  COMPONENT = ShapeFlags.STATEFUL_COMPONENT | ShapeFlags.FUNCTIONAL_COMPONENT // 判断是不是组件 00000110
  // 00000100 ｜ 00000010 = 00000110
}

